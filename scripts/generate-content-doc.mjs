function h1(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 480, after: 120 },
    border: { bottom: { style: BorderStyle.SINGLE, size: 6, color: "E11D48", space: 4 } },
  });
}

function h2(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 320, after: 80 },
  });
}

function h3(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 200, after: 60 },
  });
}

function label(text) {
  return new Paragraph({
    children: [new TextRun({ text, bold: true, color: "9CA3AF", size: 18, allCaps: true })],
    spacing: { before: 160, after: 40 },
  });
}

function body(text) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22 })],
    spacing: { after: 100 },
  });
}

function bullet(text) {
  return new Paragraph({
    children: [new TextRun({ text: `• ${text}`, size: 22 })],
    spacing: { after: 60 },
    indent: { left: 360 },
  });
}

function field(labelText, value) {
  return new Paragraph({
    children: [
      new TextRun({ text: `${labelText}: `, bold: true, size: 22 }),
      new TextRun({ text: value, size: 22 }),
    ],
    spacing: { after: 80 },
  });
}

function divider() {
  return new Paragraph({
    border: { bottom: { style: BorderStyle.SINGLE, size: 2, color: "E5E7EB", space: 6 } },
    spacing: { before: 200, after: 200 },
  });
}

